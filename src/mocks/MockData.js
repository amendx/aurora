// Função auxiliar para criar um plantão mockado
const createMockShift = (id, date, label, startTime, endTime, group, isPast = false) => {
  const [year, month, day] = date.split('-');
  const monthNames = {
    '01': 'Janeiro', '02': 'Fevereiro', '03': 'Março', '04': 'Abril',
    '05': 'Maio', '06': 'Junho', '07': 'Julho', '08': 'Agosto',
    '09': 'Setembro', '10': 'Outubro', '11': 'Novembro', '12': 'Dezembro'
  };
  const dayNames = ['Domingo', 'Segunda-Feira', 'Terça-Feira', 'Quarta-Feira', 'Quinta-Feira', 'Sexta-Feira', 'Sábado'];
  
  const dateObj = new Date(parseInt(year), parseInt(month) - 1, parseInt(day));
  const dayName = dayNames[dateObj.getDay()];
  const monthName = monthNames[month];
  
  const timeDisplay = `${startTime} - ${endTime} (${label})`;
  
  return {
    "id": id,
    "is_personal": false,
    "date": `${dayName}, ${parseInt(day)} de ${monthName}`,
    "schedule": `${year}-${month}`,
    "time": timeDisplay,
    "is_past": isPast,
    "label": label,
    "vacancy": null,
    "transaction": null,
    "coworkers": [],
    "start_date": `${date} ${startTime}:00`,
    "end_date": label === 'N' ? `${year}-${String(parseInt(month)).padStart(2, '0')}-${String(parseInt(day) + 1).padStart(2, '0')} ${endTime}:00` : `${date} ${endTime}:00`,
    "is_synced_with_google_calendar": false,
    "google_calendar_event_id": null,
    "user": MOCK_USER_DATA,
    "group": group
  };
};

